def train():
    global pokemon_level
    global pokemon_name
    pokemon_level = pokemon_level + 1
    print("Your Pokemon has leveled up! " + str(pokemon_name) + " is now level " + str(pokemon_level) + " !")
    if pokemon_level == 50:
        charizard_evolution()
    elif pokemon_level == 20:
        charmeleon_evolution()

def gym_battle():
    global pokemon_level
    global pokemon_name
    global wins
    global losses
    boss_decide = (random.randint(1,20))
    if boss_decide == 5:
        print("Your Pokemon has encountered a boss!")
        boss_result = (random.randint(1,5))
        if boss_result == 1:
            pokemon_level = pokemon_level + 5
            wins = wins + 1
            print("Your Pokemon was won the boss battle! " + str(pokemon_name) + " is now level " + str(pokemon_level) + " !")
            if pokemon_level == 50 or pokemon_level == 51:
                charizard_evolution()
            elif pokemon_level == 20 or pokemon_level == 21:
                charmeleon_evolution()
        else:
            print("Your Pokemon lost the battle! ")
            losses = losses + 1
            if pokemon_level == 50 or pokemon_level == 51:
                charizard_evolution()
            elif pokemon_level == 20 or pokemon_level == 21:
                charmeleon_evolution()
    else:
        gym_result = (random.randint(1, 2))
        if gym_result == 1:
            pokemon_level = pokemon_level + 2
            print("Your Pokemon has won the battle! " + str(pokemon_name) + " is now level " + str(pokemon_level) + " !")
            wins = wins + 1
            if pokemon_level == 50 or pokemon_level == 51:
                charizard_evolution()
            elif pokemon_level == 20 or pokemon_level == 21:
                charmeleon_evolution()
        elif gym_result == 2:
            print("Your Pokemon lost the battle! ")
            losses = losses + 1
            if pokemon_level == 50 or pokemon_level == 51:
                charizard_evolution()
            elif pokemon_level == 20 or pokemon_level == 21:
                charmeleon_evolution()


